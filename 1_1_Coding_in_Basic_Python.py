# basic_python.py

# Print "Hello, World!"
print("Hello, World!")

# Variables and Data Types
x = 10
y = 3.14
name = "Alice"
is_student = True

print(f"x: {x}, y: {y}, name: {name}, is_student: {is_student}")

# Lists
fruits = ["apple", "banana", "cherry"]
print(f"Fruits: {fruits}")

# Loops
for fruit in fruits:
    print(f"I like {fruit}")

# Functions
def greet(name):
    return f"Hello, {name}!"

print(greet("Bob"))
